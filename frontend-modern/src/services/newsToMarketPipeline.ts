// ==================== 核心流水线类 ====================

export class NewsToMarketPipeline {
  private processedEventIds: Set<string> = new Set()
  private generatedMarkets: GeneratedMarket[] = []
  private lastRunTime: Date | null = null

  /**
   * 从新闻标题提取事件
   */
  extractEvent(newsTitle: string, newsUrl?: string): ExtractedEvent | null {
    // Extract events by matching patterns
    for (const rule of EXTRACTION_RULES) {
      const match = newsTitle.match(rule.pattern)
      if (match) {
        const baseCertainty = 0.5 + rule.certaintyBoost
        const certainty = Math.min(1.0, baseCertainty + (this.calculateNewsCertainty(newsTitle) * 0.2))
        
        const event: ExtractedEvent = {
          id: this.generateEventId(newsTitle, rule.type),
          type: rule.type,
          subject: rule.subjectExtractor(match),
          action: rule.actionExtractor(match),
          details: newsTitle,
          window: this.formatWindow(rule.windowDays),
          windowDays: rule.windowDays,
          certainty,
          sourceNews: newsTitle,
          sourceUrl: newsUrl,
          extractedAt: new Date(),
          tier: rule.tier
        }
        
        return event
      }
    }
    
    return null
  }

  /**
   * 从事件生成预测市场
   */
  generateMarketFromEvent(event: ExtractedEvent): GeneratedMarket | null {
    // 检查是否已处理
    if (this.processedEventIds.has(event.id)) {
      return null
    }

    // 找到匹配的模板
    const template = this.findBestTemplate(event)
    if (!template) {
      return null
    }

    // 计算结束日期
    const endDate = this.calculateEndDate(event)
    
    // 生成问题文本
    const question = this.formatQuestion(template.questionEn, event)
    const questionZh = this.formatQuestion(template.questionZh, event)
    const description = this.formatQuestion(template.descriptionEn, event)
    const descriptionZh = this.formatQuestion(template.descriptionZh, event)

    // 计算初始价格（基于置信度）
    const yesPrice = this.calculateInitialPrice(event)
    
    const market: GeneratedMarket = {
      id: `auto_${event.tier.toLowerCase()}_${Date.now()}_${Math.random().toString(36).substr(2, 6)}`,
      tier: event.tier,
      question,
      questionZh,
      description,
      descriptionZh,
      category: template.category,
      yesPrice,
      noPrice: 1 - yesPrice,
      endDate,
      volume: this.estimateInitialVolume(event),
      participants: this.estimateInitialParticipants(event),
      sourceEvent: event,
      template: template.id,
      createdAt: new Date(),
      isAutoGenerated: true
    }

    // 标记为已处理
    this.processedEventIds.add(event.id)
    this.generatedMarkets.push(market)

    return market
  }

  /**
   * 批量处理新闻列表
   */
  processNewsBatch(newsList: Array<{ title: string; url?: string; date?: string }>): GeneratedMarket[] {
    const markets: GeneratedMarket[] = []
    const now = new Date()
    
    for (const news of newsList) {
      // 只处理最近 72 小时的新闻
      if (news.date) {
        const newsDate = new Date(news.date)
        const hoursDiff = (now.getTime() - newsDate.getTime()) / (1000 * 60 * 60)
        if (hoursDiff > 72) continue
      }

      const event = this.extractEvent(news.title, news.url)
      if (event && event.certainty >= 0.5) {  // 只处理高置信度事件
        const market = this.generateMarketFromEvent(event)
        if (market) {
          markets.push(market)
        }
      }
    }

    this.lastRunTime = now
    return markets
  }

  /**
   * 获取按层级分组的市场
   */
  getMarketsByTier(): { L1: GeneratedMarket[], L2: GeneratedMarket[], L3: GeneratedMarket[] } {
    return {
      L1: this.generatedMarkets.filter(m => m.tier === 'L1'),
      L2: this.generatedMarkets.filter(m => m.tier === 'L2'),
      L3: this.generatedMarkets.filter(m => m.tier === 'L3')
    }
  }

  /**
   * 获取今日生成的市场
   */
  getTodayMarkets(): GeneratedMarket[] {
    const today = new Date()
    today.setHours(0, 0, 0, 0)
    return this.generatedMarkets.filter(m => m.createdAt >= today)
  }

  /**
   * 清除过期市场
   */
  cleanExpiredMarkets(): void {
    const now = new Date()
    this.generatedMarkets = this.generatedMarkets.filter(m => {
      const endDate = new Date(m.endDate)
      return endDate > now
    })
  }

  // ==================== 辅助方法 ====================

  private generateEventId(title: string, type: EventType): string {
    const hash = title.toLowerCase().replace(/[^a-z0-9]/g, '').substring(0, 20)
    return `${type}_${hash}_${Date.now()}`
  }

  private formatWindow(days: number): string {
    if (days <= 7) return `${days}d`
    if (days <= 30) return `${Math.ceil(days / 7)}w`
    if (days <= 90) return `${Math.ceil(days / 30)}m`
    if (days <= 180) return 'Q' + Math.ceil(days / 90)
    return `${Math.ceil(days / 365)}y`
  }

  private calculateNewsCertainty(title: string): number {
    // 根据关键词增加置信度
    let boost = 0
    const lowerTitle = title.toLowerCase()
    
    if (lowerTitle.includes('official') || lowerTitle.includes('confirm')) boost += 0.15
    if (lowerTitle.includes('breaking') || lowerTitle.includes('just in')) boost += 0.1
    if (lowerTitle.includes('announce') || lowerTitle.includes('sign')) boost += 0.1
    if (lowerTitle.includes('may') || lowerTitle.includes('might') || lowerTitle.includes('could')) boost -= 0.1
    if (lowerTitle.includes('rumor') || lowerTitle.includes('speculation')) boost -= 0.2
    
    return Math.max(0, Math.min(0.5, boost))
  }

  private findBestTemplate(event: ExtractedEvent): MarketTemplate | null {
    // 首先按类型和层级匹配
    const matches = MARKET_TEMPLATES.filter(t => t.type === event.type && t.tier === event.tier)
    if (matches.length > 0) {
      return matches[0]
    }
    
    // 其次只按类型匹配
    const typeMatches = MARKET_TEMPLATES.filter(t => t.type === event.type)
    if (typeMatches.length > 0) {
      return typeMatches[0]
    }
    
    return null
  }

  private calculateEndDate(event: ExtractedEvent): string {
    const now = new Date()
    const endDate = new Date(now.getTime() + event.windowDays * 24 * 60 * 60 * 1000)
    return endDate.toISOString().split('T')[0]
  }

  private formatQuestion(template: string, event: ExtractedEvent): string {
    const endDate = this.calculateEndDate(event)
    const formattedDate = new Date(endDate).toLocaleDateString('en-US', { month: 'short', day: 'numeric' })
    
    return template
      .replace(/{subject}/g, event.subject)
      .replace(/{action}/g, event.action)
      .replace(/{date}/g, formattedDate)
      .replace(/{window}/g, event.window)
      .replace(/{price}/g, '100')  // 默认价格
      .replace(/{threshold}/g, '25') // 默认阈值
  }

  private calculateInitialPrice(event: ExtractedEvent): number {
    // 基于置信度设置初始价格
    // 高置信度 = 更接近 0.5（更不确定）
    // 低置信度 = 更极端的价格
    const base = 0.5
    const deviation = (1 - event.certainty) * 0.3
    const randomBias = (Math.random() - 0.5) * 0.2
    
    return Math.max(0.1, Math.min(0.9, base + deviation * (Math.random() > 0.5 ? 1 : -1) + randomBias))
  }

  private estimateInitialVolume(event: ExtractedEvent): number {
    const baseVolume: Record<MarketTier, number> = {
      'L1': 500000,
      'L2': 1000000,
      'L3': 2000000
    }
    const multiplier = 0.5 + Math.random()
    return Math.floor(baseVolume[event.tier] * multiplier * event.certainty)
  }

  private estimateInitialParticipants(event: ExtractedEvent): number {
    const baseParticipants: Record<MarketTier, number> = {
      'L1': 500,
      'L2': 1000,
      'L3': 2000
    }
    const multiplier = 0.5 + Math.random()
    return Math.floor(baseParticipants[event.tier] * multiplier)
  }
}


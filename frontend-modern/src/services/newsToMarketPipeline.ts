/**
 * News-to-Market Pipeline / 新闻到市场生成流水线
 * 
 * 这是核心的事件驱动预测市场生成器
 * 
 * 流水线: 新闻 → 事件(Event) → 可验证判断(Outcome) → 预测市场(Market)
 * 
 * 三层市场架构:
 * - L1: 短期新闻驱动 (1-30天) - 每日更新
 * - L2: 中期事件 (1-6个月) - 季度级
 * - L3: 长期叙事 (6个月-2年) - 宏观趋势
 */

// ==================== 类型定义 ====================

export type EventType = 
  | 'policy'      // 政策 (tariffs, regulations, executive orders)
  | 'rate'        // 利率决策 (Fed, ECB, BOJ)
  | 'earnings'    // 财报 (quarterly reports)
  | 'tariff'      // 关税 (trade actions)
  | 'legal'       // 法律 (lawsuits, rulings, sanctions)
  | 'geopolitical'// 地缘政治 (conflicts, diplomacy)
  | 'tech'        // 科技 (product launches, AI developments)
  | 'market'      // 市场 (price movements, IPOs)
  | 'election'    // 选举 (voting, polls)
  | 'crypto'      // 加密货币 (regulations, ETFs)

export type MarketTier = 'L1' | 'L2' | 'L3'

export interface ExtractedEvent {
  id: string
  type: EventType
  subject: string           // "Trump", "ECB", "NVIDIA", "China"
  action: string            // "announce", "raise", "ban", "file"
  details: string           // 具体内容
  window: string            // "3d", "7d", "30d", "Q2", "2026"
  windowDays: number        // 数值化的天数
  certainty: number         // 0.0 - 1.0 置信度
  sourceNews: string        // 原始新闻标题
  sourceUrl?: string        // 新闻链接
  extractedAt: Date         // 提取时间
  tier: MarketTier          // L1/L2/L3
}

export interface GeneratedMarket {
  id: string
  tier: MarketTier
  question: string          // 英文问题
  questionZh: string        // 中文问题
  description: string
  descriptionZh: string
  category: string
  yesPrice: number
  noPrice: number
  endDate: string           // YYYY-MM-DD
  volume: number
  participants: number
  sourceEvent: ExtractedEvent
  template: string          // 使用的模板ID
  createdAt: Date
  isAutoGenerated: boolean
}

